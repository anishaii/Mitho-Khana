    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
    //if single row is selected than update

    String sn = snfield.getText();
    String name = foodField.getText();
    String category = categoryField.getText();
    String prep = prepField.getText();
    String ingredients = ingreField.getText();
    String diet = dietaryField.getText();

    //set updated value on table row
    tblModel.setValueAt(sn, jTable1.getSelectedRow(), 0);
    tblModel.setValueAt(name, jTable1.getSelectedRow(), 1);
    tblModel.setValueAt(category, jTable1.getSelectedRow(), 2);
    tblModel.setValueAt(prep, jTable1.getSelectedRow(), 3);
    tblModel.setValueAt(ingredients, jTable1.getSelectedRow(), 4);
    tblModel.setValueAt(diet, jTable1.getSelectedRow(), 5);

    //update message display
    JOptionPane.showMessageDialog(this, "Update Successfully.");
    } else{
            if(jTable1.getRowCount()==0) {  //if table is empty
    JOptionPane.showMessageDialog(this, "Table is Empty..");
    }else{                          
    //if row is not selected or multiple row is selected than
    JOptionPane.showMessageDialog(this, "Please select Single Row For Update..");
    }
        }
        
        
        
        
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        
        //delete row
        if(jTable1.getSelectedRowCount()==1) {
        //if single row is selected than delete
        tblModel.removeRow(jTable1.getSelectedRow());
         } else {
        if(jTable1.getRowCount()==0) {
        // if table is empty (no data) than display message
        JOptionPane.showMessageDialog(this, "Table is Empty.");
         } else {
        // if table is not empty but row is not selected or multiple row is selected
    JOptionPane.showMessageDialog(this,"Please select Single Row For Deletion");
    }
}
        
            
    }//GEN-LAST:event_deleteButtonActionPerformed

