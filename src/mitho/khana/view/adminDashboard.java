    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if(snfield.getText().equals("")||foodField.getText().equals("")||categoryField.getText().equals("")|| prepField.getText().equals("")||ingreField.getText().equals("")||dietaryField.getText().equals("")){
           JOptionPane.showMessageDialog(this,"Please enter all the data !!!");
        }else{
            String data[]= {snfield.getText(),foodField.getText(),categoryField.getText(), prepField.getText(),ingreField.getText(),dietaryField.getText()};
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        tblModel.addRow(data);
        JOptionPane.showMessageDialog(this,"Add data successfully !!!");
        snfield.setText("");
        foodField.setText("");
        categoryField.setText("");
        prepField.setText("");
        ingreField.setText("");
        dietaryField.setText("");
        
            
        }
       
        
        
        
    }//GEN-LAST:event_addButtonActionPerformed

